export function sum(a, b) {
  return a + b;
}

export function subtract(a, b) {
  return a - b;
}

export function multiply(a, b) {
  return a * b;
}

export function divide(a, b) {
  return a / b;
}

export function increase(a) {
  let res = a;
  res++;

  return res;
}

export function decrease(a) {
  let res = a;
  res--;

  return res;
}

export function modulus(a, b) {
  return a % b;
}
